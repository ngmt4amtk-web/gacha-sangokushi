window.Game = window.Game || {};

// Stat multiplier by character rarity (lower rarity = bigger boost)
Game.getSignatureMultiplier = function(rarity) {
  return {1:5.0, 2:2.5, 3:2.0, 4:1.8, 5:1.5}[rarity] || 1.5;
};

// Skill boost by character type
Game.getSignatureBoost = function(type) {
  switch(type) {
    case 0: return {multScale:2.0, critBonus:0.30};          // 武: skill×2 + crit+30%
    case 1: return {activation:1.0, everyTurn:true};          // 智: 100% activation every turn
    case 2: return {doubleAttack:true, spdMult:1.5};          // 速: double attack + SPD×1.5
    case 3: return {defMult:1.5, autoCounter:0.50};           // 守: DEF×1.5 + 50% counter
    default: return {multScale:1.5};
  }
};

